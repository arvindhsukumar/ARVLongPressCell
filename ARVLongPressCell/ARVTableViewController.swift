    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("Cell", forIndexPath: indexPath) as UITableViewCell
        
        cell.textLabel!.text = "Test"
        let lpgr = UILongPressGestureRecognizer(target: self, action: "longPressedCell:")

        
        cell.backgroundColor = UIColor(red: CGFloat(arc4random_uniform(255))/255.0, green: CGFloat(arc4random_uniform(255))/255.0, blue: CGFloat(arc4random_uniform(255))/255.0, alpha: 1)

        if cell.gestureRecognizers == nil{
            cell.addGestureRecognizer(lpgr)
        }
        // Configure the cell...

        return cell
    }
    
    func longPressedCell(lpgr:UILongPressGestureRecognizer){

        if lpgr.state == UIGestureRecognizerState.Began {
            let view = lpgr.view as UITableViewCell
            view.setHighlighted(false , animated: false)
            self.view.superview?.addSubview(self.longPressView)
            self.longPressView.showView(view)

        }
    }
